  <h3> Planetary Sciences </h3>

  <p class="alt-paragraph" align="justify" style="box-sizing: border-box;margin-top: 0;margin-bottom: 1rem;orphans: 3;widows: 3;"><img src="http://www.naic.edu/ao/sites/default/files/planetary.png" style="padding-bottom: 20px;box-sizing: border-box;vertical-align: middle;border-style: none;page-break-inside: avoid;width:80px;" hspace="20" align="left" >
“The Arecibo Planetary Radar is used to study celestial bodies in our solar system such as planets, moons, asteroids and comets. Directed by the 305-meter reflector, a powerful beam of radio energy is transmitted in the direction of the target object. A small portion of this energy is reflected by the target, back in the direction of the Earth. This radio echo is processed then analyzed to yield information about the size, shape, spin, density, composition, surface properties, geology (e.g., ridges, craters, and boulders) of the object, and also allows to identify binary and triple asteroid systems. The Arecibo Planetary Radar System can measure the distance to an asteroid, typically millions of km away, with a precision of meters, and it can measure the speed of an asteroid, typically tens of kilometers per second, with a precision of millimeters per second. Arecibo’s precision can greatly refine asteroid orbits, aiding NASA in its congressionally mandated mission to study near-Earth objects to support planetary defense.” </p>

